package com.app;
//Using Brute Force
public class EvenOrOddUsingBruteForce {

	public static void main(String[] args) {
//		if (number % 2 == 0)
//			if yes, print “Even number”
//			if not, print “Odd number”
		int num=29;
		if(num%2==0) {
			System.out.println(num + "is even");
		}
		else
			System.out.println(num + "is odd");
	}

}
