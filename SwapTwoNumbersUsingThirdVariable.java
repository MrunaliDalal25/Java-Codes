import static java.lang.System.out;

public class SwapTwoNumbersUsingThirdVariable {
    public static void main(String[] args) {
        int a = 36,b = 69,c;
        c = a;
        a = b;
        b = c;
        System.out.println("Swapped Successfully:");
        System.out.println("a = " + a);
        System.out.println("b = " + b);


    }

}
